package com.sprintqa.java.class7;

public class TypeCasting {

	public static void main(String[] args) {
        
		
		//Implicit Type casting
		//widening
		int num1 = 10;
		double num2;
		num2 = num1;
		
		//Explicit type casting
		//narrowing
		
		//double num1 = 10.0;
        // int num2;
		// num2 = (int)num1 ; //We are covering double data
         
         
         num2 = (int) num1;
         System.out.print(num2);
		
		
		
		
	}

}
